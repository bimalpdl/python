# Arithmetic operators in Python
# + , - , * , / , % , // , **
print(10/3)   # this will output floating points as result( 3.333..)
print(10//3)  # Floor Division // produces integer (3)
print(2**3)   # 2^3   // power operator

num1 = 11
num2 = 3
print("All the operations are performed on {} and {}".format(num1,num2))
print("Sum:", num1 + num2)
print("Subtraction:", num1 - num2)
print("Multiplication:", num1 * num2)
print("Division:", num1 / num2)
print("Modulo:", num1 % num2)
print("Floor division:", num1 // num2)
print("Power:", num1 ** num2)
