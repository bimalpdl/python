# 'and', 'or', 'not' keywords are used for AND,OR,NOT operations (these are equivalent of &&, ||, ! operators in C++)
print(True and True)  # True
print(True or Flase)   # False
print(not True)    # False


# Bitwise operators
# & -> bitwise AND, | -> bitwise OR, ~ -> bitwise NOT,  ^ -> bitwise XOR, << -> bitwise left shift, >> -> bitwise right shift
print(2&3)  # 0010 & 0011 = 0010  = 2
print(2|3)
print(~3)
print(2^3)
print(2<<1)    # left shift 0010 = 2 by 1 bit => 0100 = 4
print(4>>1)    # right shift 0100 = 4 by 1 bit => 0010 = 2
